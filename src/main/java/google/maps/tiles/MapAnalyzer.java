package google.maps.tiles;

public class MapAnalyzer {

    private final int minX;
    private final int maxX;
    private final int minY;
    private final int maxY;

    public MapAnalyzer(int minX, int maxX, int minY, int maxY) {
        this.minX = minX;
        this.maxX = maxX;
        this.minY = minY;
        this.maxY = maxY;
    }


}
